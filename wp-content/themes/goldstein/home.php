                <h3>Last Will and Testament</h3>
                <p>A valid legally enforceable Last Will and Testament (“Will”) insures that your wishes regarding the
                    disposition of your assets upon your death will be followed. Don’t make the mistake of thinking that
                    you can simply sign and notarize a letter or other statement of your desires as to who will inherit
                    your property. New York estates law requires that a Will satisfy the statutory requirements, in
                    order to be valid and enforceable. There is little reason to save an attorney’s legal fee in
                    preparing a Will, and risk leaving your heirs the substantial inconvenience, uncertainty and expense
                    of a Surrogate’s Court litigation to determine who will receive your assets after death.</p>
                <h3>Living Will and Health Care Proxy</h3>
                <p>In addition to executing a properly prepared Will to protect your loved ones, you may also want to
                    protect yourself with a Living Will and Health Care Proxy. These documents can explicitly state your
                    desires regarding whether or not to keep you alive on a respirator if there is no chance of
                    recovery, whether you should spend your last days at home or in a hospital, etc. The Health Care
                    Proxy enables you to designate a trusted friend or relative to make health care decisions for you,
                    if you are mentally or physically unable to make or express those opinions.</p>
                <h3>Power of Attorney</h3>
                <p>A Power of Attorney enables you to legally appoint a trusted family member, friend, or other person
                    you designate to sign legal documents on your behalf. For example, a Power of Attorney is useful if
                    you have entered into a contract to purchase or sell real estate, and you will be traveling out of
                    the country when the closing of title will take place. With a Power of Attorney, you can grant
                    authority to your designated representative to sign legal documents on your behalf at the closing of
                    title, and to pay or collect the required sums of money to complete the transaction on your behalf.
                </p>
